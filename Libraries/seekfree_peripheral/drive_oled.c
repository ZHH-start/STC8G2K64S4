// 连续写数据进入OLED
char writeOledBuf(char addr, unsigned char *date, int len)
{
    // return  iic_Wirte_Date(0x78,adds,date,len);
    iicWriteDate(0x78, addr, date, len);
    return 0;
}

//   写入OLED 命令
void writeOledCmd(unsigned char cmd)
{
    // iic_Wirte_Reg(0x78,0x00,cmd);
    iic_write_reg(0x78, 0x00, cmd);
    //	iic_write_reg(0x3c,0x80,cmd);
}

//   写入OLED 单个数据
void writeOledData(unsigned char dat)
{
    // iic_Wirte_Reg(0x78,0x40,dat);
    iic_write_reg(0x78, 0x40, dat);
}

//  设置坐标
void setOledPoint(int x, int y)
{
    writeOledCmd(0xb0 + y);
    writeOledCmd(((x & 0xf0) >> 4) | 0x10);
    writeOledCmd((x & 0x0f));
}
